#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	c;

	c = 0;
	if (lst != NULL)
	{
		c = 1;
		while (lst->next != NULL)
		{
			lst = lst->next;
			c++;
		}
	}
	return (c);
}
